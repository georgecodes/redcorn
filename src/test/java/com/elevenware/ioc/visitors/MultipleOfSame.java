package com.elevenware.ioc.visitors;

public class MultipleOfSame {

    private String first;
    private String second;

    public MultipleOfSame(String first, String second) {
        this.first = first;
        this.second = second;
    }

    public String getFirst() {
        return first;
    }

    public String getSecond() {
        return second;
    }
}
